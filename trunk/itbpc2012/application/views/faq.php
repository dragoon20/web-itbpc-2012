<b>Seperti apa soal-soal <b style="color:orange">ITB PROGRAMMING CONTEST 2012</b>?</b>
<ul>
	<li>
		Semua permasalahan yang diberikan di <b style="color:orange">ITB PROGRAMMING CONTEST 2012</b> dapat dipecahkan dengan algoritma yang pasti (deterministik). Pada beberapa soal, efisiensi memainkan peran yang cukup penting. Contoh soal-soal dapat dilihat pada <a href="http://tokilearning.org/">TOKI Learning Center</a>.</li>
	<li>
		Bahasa yang dapat digunakan adalan <b>Pascal, C, dan C++</b>.</li>
</ul> 

