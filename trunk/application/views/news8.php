<p>
	Daftar nilai keseluruhan babak penyisihan dapat dilihat pada link <a href="http://itbpc.org/scoreboard/jpc">Scoreboard Penyisihan Junior Programming Contest</a>, dan <a href="http://itbpc.org/scoreboard/spc">Scoreboard Penyisihan Senior Programming Contest.</a>

</p>
<p>
	Segenap panitia ITB Programming Contest mengucapkan selamat kepada seluruh peserta yang telah berani berkompetisi dalam ajang ITB Programming Contest 2011. Sebagai bentuk apresiasi panitia terhadap peserta, peserta akan diberikan sertifikat keikutsertaan dalam bentuk <em>soft-copy</em> yang akan dikirimkan ke email masing-masing paling lambat 1 November 2011.
</p>
