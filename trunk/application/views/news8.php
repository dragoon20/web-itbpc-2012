
<h2 style="margin :0px"><a href="#">Hasil Babak Penyisihan</a></h2>
<i>17 Oktober 2011</i><br/>

<p>
	Setelah melalui pertarungan yang sengit pada babak penyisihan ITB Junior Programming Contest dan ITB Senior Programming Contest yang diadakan pada hari Sabtu, dan Minggu, 15 dan 16 Oktober 2011, diperoleh tim-tim dan peserta-peserta terbaik yang diundang untuk bertanding pada Final ITB Programming Contest pada hari Selasa dan Rabu, tanggal 8 dan 9 November 2011. Adapun hasil berikut adalah final dan tidak dapat diganggu gugat.
</p>
